from django.contrib import admin
from django.urls import path, include
from shareProf.api.views import auth_views
from rest_framework import routers

router = routers.DefaultRouter()
router.register(r'users', auth_views.UserViewSet)
router.register(r'groups', auth_views.GroupViewSet)

urlpatterns = [
    path('', include(router.urls)),
    path('admin/', admin.site.urls),
    path('api-auth/', include('rest_framework.urls', namespace='rest_framework')),#Agrega el boton de logout y login
    path('api/', include('shareProf.api.urls'))
]
